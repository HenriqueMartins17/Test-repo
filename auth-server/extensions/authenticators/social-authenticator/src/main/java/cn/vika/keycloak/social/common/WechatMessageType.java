/**
 * <p>
 * 微信消息类型
 * </p>
 *
 * @author Benson Cheung
 * @date 2020/02/21 16:48
 */
public enum WechatMessageType {

    /**
     * 文本类型
     */
    TEXT,

    /**
     * 图片类型
     */
    IMAGE,

    /**
     * 公众号规则同步：图片类型
     */
    IMG,

    /**
     * 语音类型
     */
    VOICE,

    /**
     * 视频类型
     */
    VIDEO,

    /**
     * 小视频类型
     */
    SHORTVIDEO,

    /**
     * 音乐类型
     */
    MUSIC,

    /**
     * 地理位置类型
     */
    LOCATION,

    /**
     * 链接类型
     */
    LINK,

    /**
     * 图文类型
     */
    NEWS,

    /**
     * 事件类型
     */
    EVENT;

}
